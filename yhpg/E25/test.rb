require 'pry'
require_relative './solver.rb'

$dbg = false
ng_c = 0
lines = DATA.read.split("\n")
lines.each.with_index(0) do |line, idx|
  # $dbg = true
  # next unless idx == 7
  _, input, expected = line.match(/"(.+)", "(.+)"/).to_a
  solver = Solver.new
  actual = solver.solve(input)
  if actual == expected
    puts "ok #{idx}"
  else
    puts "#{idx}"
    puts "input   : #{input}"
    puts "actual  : #{actual}"
    puts "expected: #{expected}"
    ng_c += 1
  end
end
puts "NG: #{ng_c}"

